Route::get('/forgot-password', function () {
    return view('auth.forgot-password');
})->middleware('guest')->name('password.request');

Route::post('/forgot-password', function (Request $request) {
    $request->validate(['email' => 'required|email']);

    $status = Password::sendResetLink(
        $request->only('email')
    );

    return $status === Password::RESET_LINK_SENT
        ? back()->with(['status' => 'Линкот за ресетирање на лозинката е испратен на вашата е-пошта.'])
        : back()->withErrors(['email' => 'Не можеме да најдеме корисник со таа е-пошта.']);
})->middleware('guest')->name('password.email');

Route::post('/password-reset', function (Request $request) {
    $request->validate([
        'token' => 'required',
        'email' => 'required|email',
        'password' => 'required|min:8|confirmed',
    ]);

    $status = Password::reset(
        $request->only('email', 'password', 'password_confirmation', 'token'),
        function (User $user, string $password) {
            $user->forceFill([
                'password' => Hash::make($password)
            ])->setRememberToken(Str::random(60));

            $user->save();

            event(new PasswordReset($user));
        }
    );

    return $status === Password::PASSWORD_RESET
        ? redirect()->route('login')->with('status', 'Вашата лозинка е успешно ресетирана.')
        : back()->withErrors(['email' => 'Дојде до грешка при ресетирањето на лозинката. Обидете се повторно.']);
})->middleware('guest')->name('password.update');


